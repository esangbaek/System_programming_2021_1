STRING	START	0x2000
FIRST	STL	RETADD
	LDB	#LENGTH
	BASE	LENGTH
	CLEAR	A
	CLEAR	X
	COMP	=2048
	JEQ	FINISH
LOOP	+LDCH	BUFONE,X
	RMO	A,T
	+LDCH	BUFTWO,X
	RMO	A,S
	COMPR	T,S
	JLT	JMPONE
	JGT	JMPTWO
	TIX	=2048
	JLT	LOOP
FINISH	J	@RETADD
JMPONE	+JSUB	PRINTA
	J	LOOP
JMPTWO	+JSUB	PRINTB
	J	LOOP

	LTORG
	=2048	800


RETADD	RESW	1
LENGTH	RESW	1
BUFONE	RESW	2048
BUFTWO	RESW	2048

PRINTA	TD	DEVA
	JEQ	PRINTA
	RMO	T,A
	WD	DEVA
	RSUB
DEVA	BYTE	X'10'

PRINTB	TD	DEVB
	JEQ	PRINTB
	RMO	S,A
	WD	DEVB
	RSUB
DEVB	BYTE	X'11'
	END	STRING